"""
Write a Python script that takes input from the user and displays that input 
back in upper and lower cases. 
"""

user_input = input("Enter a string:::   ")
input_in_upper_case = user_input.upper()
input_in_lower_case = user_input.lower()
print('Upper case:: {}'.format(input_in_upper_case))
print('Lower case:: {}'.format(input_in_lower_case))
